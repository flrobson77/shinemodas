        <p>
        Segundo Isabella Potter do portal ONDM a moda Plus Size vem cada vez mais ganhando força no Brasil! O termo Plus Size foi criado na década de 70 nos Estados Unidos, dentro da própria indústria de moda, para designar os manequins acima de 44, porém, começou a ganhar conhecimento na década de 90. As mulheres que são consideradas Plus Size atualmente, já foram o ideal de corpo feminino em diversos períodos históricos. Apesar de muitas pessoas falarem que estar acima do peso faz mal para a saúde, a moda plus size não surgiu para incentivar o excesso de peso e sim, para valorizar a diversidade e a curva dos corpos, indo contra os padrões de beleza e a ditadura da magreza.    
        </p>
        <p> Um aspecto importante do conceito de moda plus size é o empoderamento. Ele visa capacitar as pessoas a abraçarem seus corpos e expressarem sua individualidade através das roupas. A moda plus size oferece uma ampla gama de estilos, desde peças clássicas e elegantes até tendências modernas, permitindo que cada pessoa crie seu próprio estilo único.</p>
        <p> Além disso, a moda plus size promove a inclusão e a representatividade. Ela reconhece a diversidade de corpos e busca eliminar a marginalização das pessoas que usam tamanhos maiores. Isso é alcançado por meio da inclusão de modelos plus size nas campanhas publicitárias, da diversidade de tamanhos nas opções de vestuário e do diálogo sobre a importância da aceitação e do respeito por todos os tipos de corpos.</p>
        
